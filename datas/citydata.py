import numpy as np
from numpy import random


def city_random_generator(nums):
    """
    随机生成nums个城市数据
    :param nums: 城市个数
    :return: nums个城市数据
    """
    citys = []
    n = 0
    while n < nums:
        x = random.randn()
        y = random.randn()
        if [x, y] not in citys:
            citys.append([x, y])
            n = n + 1
    return np.array(citys)


