def china_citys():
    """
    中国34个城市的经纬度数据
    :return: 34个城市数据
    """
    citys = []
    f = open("./datas/ChinaCitys.txt", "r", encoding='UTF-8')
    while True:
        city = str(f.readline())
        if not city: break
        city = city.replace("\n", "")
        city = city.split("\t")
        citys.append([float(city[1]), float(city[2])])
    return np.array(citys)